function createResponsible(
  displayName: string,
  firstName = "",
  lastName = "",
  documentId = "",
): PaymentResponsible {
  return {
    displayName,
    firstName,
    lastName,
    documentId,
    taxId: "",
    nis: "",
    meterNumber: "",
  };
}

function createSpace(
  space: Omit<BuildingSpace, "hasParking" | "parkingFee"> &
    Partial<Pick<BuildingSpace, "hasParking" | "parkingFee">>,
): BuildingSpace {
  return {
    hasParking: false,
    parkingFee: 0,
    ...space,
  };
}

function createManualExpense(expense: ExpenseRecord): ExpenseRecord {
  return expense;
}

